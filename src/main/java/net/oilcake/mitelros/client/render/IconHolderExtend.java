package net.oilcake.mitelros.client.render;

import net.minecraft.Icon;

public class IconHolderExtend {
   private Icon texture_back;
   private Icon texture_bottom;
   private Icon texture_front;
   private Icon texture_side;
   private Icon texture_top;

   public Icon getTexture_back() {
      return this.texture_back;
   }

   public void setTexture_back(Icon texture_back) {
      this.texture_back = texture_back;
   }

   public Icon getTexture_bottom() {
      return this.texture_bottom;
   }

   public void setTexture_bottom(Icon texture_bottom) {
      this.texture_bottom = texture_bottom;
   }

   public Icon getTexture_front() {
      return this.texture_front;
   }

   public void setTexture_front(Icon texture_front) {
      this.texture_front = texture_front;
   }

   public Icon getTexture_side() {
      return this.texture_side;
   }

   public void setTexture_side(Icon texture_side) {
      this.texture_side = texture_side;
   }

   public Icon getTexture_top() {
      return this.texture_top;
   }

   public void setTexture_top(Icon texture_top) {
      this.texture_top = texture_top;
   }
}
